HISTORY_SAMPLE = """\
# Processed PDFs

Auto-generated by ocr-router — one section per day, one run per sub-heading.


---

## 2026-04-15

### 09:12 — 3 file(s) moved

| # | Original File | Category | Issuer | New Name | Amount | Destination |
| --- | --- | --- | --- | --- | --- | --- |
| 1 | chase_apr.pdf | Bank Account & Statements | Chase Checking | 2026.04 - Chase Checking Statement.pdf | $1,200.00 | Bank Account & Statements\\Chase Checking\\2026\\2026.04 - Chase Checking Statement.pdf |
| 2 | fpl_bill.pdf | Bills | FPL | 2026.04 - FPL Monthly - $182.40.pdf | $182.40 | Bills\\FPL\\2026\\2026.04 - FPL Monthly - $182.40.pdf |
| 3 | tricky\\|name.pdf | Receipts | — | 2026.04.15 - Receipt.pdf | — | Receipts\\2026\\2026.04.15 - Receipt.pdf |


---

## 2026-05-01

### 14:00 — 1 file(s) renamed in place

| # | Original File | Category | Issuer | New Name | Amount | Final Path |
| --- | --- | --- | --- | --- | --- | --- |
| 1 | amex_stmt.pdf | Credit Card Statements | AMEX | 2026.04 - AMEX Statement - $987.65.pdf | $987.65 | C:\\Users\\<user>\\downloads\\2026.04 - AMEX Statement - $987.65.pdf |
"""


def _make_blank_pdf(path: Path) -> Path:
    pdf = PdfWriter()
    pdf.add_blank_page(width=200, height=200)
    with open(path, "wb") as f:
        pdf.write(f)
    return path



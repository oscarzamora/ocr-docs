def test_parse_history_returns_empty_on_missing_file(temp_dir):
    assert parse_processed_history(temp_dir / "nope.md") == []


def test_parse_history_extracts_all_rows(temp_dir):
    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")

    entries = parse_processed_history(hist)

    assert len(entries) == 4
    names = [e.original_filename for e in entries]
    assert "chase_apr.pdf" in names
    assert "fpl_bill.pdf" in names
    # The escaped pipe gets restored
    assert any("tricky|name.pdf" == e.original_filename for e in entries)
    assert "amex_stmt.pdf" in names


def test_parse_history_assigns_day_and_action_mode(temp_dir):
    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")
    entries = parse_processed_history(hist)

    chase = next(e for e in entries if e.original_filename == "chase_apr.pdf")
    assert chase.day == "2026-04-15"
    assert chase.action_mode == "move"
    assert chase.category == "Bank Account & Statements"
    assert chase.issuer == "Chase Checking"
    assert "Chase Checking" in chase.destination

    amex = next(e for e in entries if e.original_filename == "amex_stmt.pdf")
    assert amex.day == "2026-05-01"
    assert amex.action_mode == "rename"
    assert amex.issuer == "AMEX"


def test_parse_history_treats_dash_as_none(temp_dir):
    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")
    entries = parse_processed_history(hist)

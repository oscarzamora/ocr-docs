def test_bootstrap_matches_files_and_appends_records(temp_dir):
    # arrange
    source = temp_dir / "downloads"
    source.mkdir()
    _make_blank_pdf(source / "chase_apr.pdf")
    _make_blank_pdf(source / "fpl_bill.pdf")
    _make_blank_pdf(source / "not_in_history.pdf")

    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")

    log = FeedbackLog(temp_dir / "_feedback" / "corrections.jsonl")

    # act — skip OCR so the test doesn't need Tesseract; blank PDFs have no text
    stats = bootstrap_from_downloads(
        source_dir=source,
        history_path=hist,
        feedback_log=log,
        ocr_engine=None,
        skip_ocr=True,
    )

    # assert
    assert stats.scanned == 3
    assert stats.matched == 2
    assert stats.appended == 2
    assert stats.no_history_match == 1

    records = list(log.iter_records())
    assert len(records) == 2
    cats = {r["final_category"] for r in records}
    assert cats == {"Bank Account & Statements", "Bills"}
    backends = {r["backend"] for r in records}
    assert backends == {"bootstrap"}
    events = {r["event"] for r in records}
    assert events == {"confirmed"}


def test_bootstrap_is_idempotent(temp_dir):
    source = temp_dir / "downloads"
    source.mkdir()
    _make_blank_pdf(source / "chase_apr.pdf")

    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")

    log = FeedbackLog(temp_dir / "log.jsonl")

    stats1 = bootstrap_from_downloads(source, hist, log, ocr_engine=None, skip_ocr=True)
    assert stats1.appended == 1

    stats2 = bootstrap_from_downloads(source, hist, log, ocr_engine=None, skip_ocr=True)
    assert stats2.appended == 0
    assert stats2.already_logged == 1
    assert len(list(log.iter_records())) == 1


def test_bootstrap_force_reimports(temp_dir):
    source = temp_dir / "downloads"
    source.mkdir()
    _make_blank_pdf(source / "chase_apr.pdf")

    hist = temp_dir / "PROCESSED_PDFS.md"
    hist.write_text(HISTORY_SAMPLE, encoding="utf-8")

    log = FeedbackLog(temp_dir / "log.jsonl")
    bootstrap_from_downloads(source, hist, log, ocr_engine=None, skip_ocr=True)
    stats = bootstrap_from_downloads(source, hist, log, ocr_engine=None, skip_ocr=True, force=True)
    assert stats.appended == 1
    assert len(list(log.iter_records())) == 2  # one from first run + one from force


def test_bootstrap_include_unprocessed_emits_pending(temp_dir):
    source = temp_dir / "downloads"
    source.mkdir()
    _make_blank_pdf(source / "mystery.pdf")

    hist = temp_dir / "PROCESSED_PDFS.md"

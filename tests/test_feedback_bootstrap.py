def test_bootstrap_from_tree_skips_excluded_dirs(temp_dir):
    root = temp_dir
    # Inside an excluded dir
    (root / "__downloads__").mkdir()
    _make_blank_pdf(root / "__downloads__" / "skipme.pdf")
    # Inside a real category
    (root / "Bills" / "FPL" / "2026").mkdir(parents=True)
    _make_blank_pdf(root / "Bills" / "FPL" / "2026" / "2026.04 - FPL.pdf")

    log = FeedbackLog(temp_dir / "log.jsonl")
    stats = bootstrap_from_tree(root, log, ocr_engine=None, skip_ocr=True)

    assert stats.scanned == 1   # only the Bills one
    assert stats.appended == 1
    records = list(log.iter_records())
    assert records[0]["final_category"] == "Bills"
    assert records[0]["final_issuer"] == "FPL"
    assert records[0]["backend"] == "bootstrap-tree"



def test_bootstrap_from_tree_is_idempotent(temp_dir):
    root = temp_dir
    (root / "Bills" / "FPL" / "2026").mkdir(parents=True)
    _make_blank_pdf(root / "Bills" / "FPL" / "2026" / "a.pdf")

    log = FeedbackLog(temp_dir / "log.jsonl")
    s1 = bootstrap_from_tree(root, log, ocr_engine=None, skip_ocr=True)
    s2 = bootstrap_from_tree(root, log, ocr_engine=None, skip_ocr=True)
    assert s1.appended == 1
    assert s2.appended == 0
    assert s2.already_logged == 1

REAL_WORLD_SAMPLE = """\
# 2026.05 — Processed Downloads

## 2026-05-15 — 25 files processed

**Notes:** stuff happened.

| # | Original File | Category | Issuer | New Name | Amount | Destination |
| --- | --- | --- | --- | --- | --- | --- |
| 1 | 911 Carrera T.pdf | — | — | 2025.12.29 - 911 Carrera T.pdf | — | __downloads__ (renamed in place) |
| 2 | canon receipt.pdf | Receipts | B&H Photo | 2025.11.18 - B&H Photo Receipt - $549.99.pdf | $549.99 | Receipts, Payment, Warranty |
| 3 | canon support page.pdf | — | — | — | — | DELETED |
| 4 | ContratoTarjetaCredito-BIP-4221.pdf | Credit Card Statements | AMEX Gold | 2024.08.10 - AMEX Gold Contract.pdf | — | Credit Card Statements\\AMEX Gold |

---

## 2026-05-25 (session 1) — 16 files processed

| #  | Original File | Category | Issuer | New Name | Amount | Destination |
| -- | ------------- | -------- | ------ | -------- | ------ | ----------- |
| 1  | 2026-04-27.pdf | Credit Card Statements | Fidelity Visa Signature | 2026.04 - Fidelity Visa (Last 9710) - $2,279.64.pdf | $2,279.64 | Credit Card Statements\\Fidelity Visa Signature\\2026 |
| 2  | 20260120-statements-5255-.pdf | Bank Account & Statements | Chase Checking | 2026.01 - Chase Checking.pdf | — | Bank Account & Statements\\Chase Checking\\2026 |

---

## Session 3 — 2026-05-25

### Duplicates deleted (16)

| File | Reason |
|---|---|
| `2026-01-27.pdf` | Already filed |

### Renamed in destination

| Location | Old Name | New Name |
|---|---|---|
| `foo` | bar | baz |

### Moved (34 files)

| # | Source | New Name | Destination |
|---|---|---|---|
| 1 | `c6f5bd7a-...pdf` | `2026.02 - Pennymac (8193759048).pdf` | `Mortgage\\PennyMac\\2026` |
"""


def test_parse_relaxed_skips_deletions(temp_dir):
    hist = temp_dir / "real.md"
    hist.write_text(REAL_WORLD_SAMPLE, encoding="utf-8")
    entries = parse_processed_history(hist)
    # 911 Carrera (no category), canon receipt, ContratoTarjetaCredito-BIP-4221,
    # 2026-04-27, 20260120-statements — 5 valid rows; DELETED row excluded,
    # Session 3 Moved table (only 4 cols) is excluded too.
    names = [e.original_filename for e in entries]
    assert "canon receipt.pdf" in names
    assert "ContratoTarjetaCredito-BIP-4221.pdf" in names
    assert "2026-04-27.pdf" in names
    assert "20260120-statements-5255-.pdf" in names
    # DELETED rows must not appear
    assert "canon support page.pdf" not in names
    # 4-col "Moved (34 files)" rows are not parseable as 7-col
    assert "c6f5bd7a-...pdf" not in names


def test_parse_relaxed_accepts_day_with_trailing_text(temp_dir):
    hist = temp_dir / "real.md"
    hist.write_text(REAL_WORLD_SAMPLE, encoding="utf-8")
    entries = parse_processed_history(hist)
    chase = next(e for e in entries if e.original_filename == "20260120-statements-5255-.pdf")
    assert chase.day == "2026-05-25"   # picks up the closest preceding day
    assert chase.category == "Bank Account & Statements"
    assert chase.issuer == "Chase Checking"


def test_parse_relaxed_session_n_day(temp_dir):
    # ``## Session 3 — 2026-05-25`` is a valid day heading via second pattern.
    hist = temp_dir / "real.md"
    hist.write_text(REAL_WORLD_SAMPLE, encoding="utf-8")
    entries = parse_processed_history(hist)
    # Session 3 is the LAST day section but contains no parseable 7-col rows,
    # so the parser falls through; no entry should claim that day.
    days = {e.day for e in entries}
    # only the two real day sections should appear
    assert days == {"2026-05-15", "2026-05-25"}


def test_parse_relaxed_strips_ocr_annotation(temp_dir):
    extra = REAL_WORLD_SAMPLE + """

## 2026-06-01 — sample

| # | Original File | Category | Issuer | New Name | Amount | Destination |
| --- | --- | --- | --- | --- | --- | --- |
| 1 | Report_001035.pdf (OCR) | Health | Florida ENT | 2026.05.14 - Florida ENT EOB.pdf | — | Health\\2026 |
"""
    hist = temp_dir / "real.md"
    hist.write_text(extra, encoding="utf-8")
    entries = parse_processed_history(hist)
    rec = next(e for e in entries if "Report_001035" in e.original_filename)
    # The " (OCR)" suffix is stripped so it matches the real on-disk filename
    assert rec.original_filename == "Report_001035.pdf"


def test_parse_history_paths_accepts_directory(temp_dir):
    f1 = temp_dir / "2026.04 - PROCESSED_PDFS.md"
    f2 = temp_dir / "2026.05 - PROCESSED_PDFS.md"
